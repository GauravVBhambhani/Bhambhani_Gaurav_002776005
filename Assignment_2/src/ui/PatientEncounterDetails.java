    private void populateTable() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Encounter hs : this.healthSystem.getEncounterHistory().getEncounterHistory()) {
            if (hs.getPatientId().equals(this.loggedInPatient.getUserName())) {
                
                String hospitalName = "";
                for (Doctor d: this.healthSystem.getDoctorDirectory().getDoctors()) {
                    if (d.getUserName().equals(hs.getDoctorId())) {
                        hospitalName = d.getHospitalName();
                    }
                }
                
                Object[] row = new Object[5];
                row[0] = hs;                     
                row[1] = hospitalName;
                row[2] = hs.getDoctorId();
                row[3] = hs.getDate();
                row[4] = hs.getTime();
                model.addRow(row);
            }

        }

    }

