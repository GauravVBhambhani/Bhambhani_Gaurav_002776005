    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = jTable1.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select an encounter to view");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Encounter encounter = (Encounter) model.getValueAt(selectedRowIndex, 0);

        String hospitalName = "";
        String doctorname = "";
        for (Doctor d: this.healthSystem.getDoctorDirectory().getDoctors()) {
            if (d.getUserName().equals(encounter.getDoctorId())) {
                hospitalName = d.getHospitalName();
                doctorname = d.getFname() + " " + d.getLname();
            }
        }

        jLabel4.setText(hospitalName);
        jLabel5.setText(doctorname);
        jLabel13.setText(String.valueOf(encounter.getVitalSign().getTemp()));
        jLabel14.setText(String.valueOf(encounter.getVitalSign().getBp()));
        jLabel15.setText(String.valueOf(encounter.getVitalSign().getPulse()));
        jLabel16.setText(String.valueOf(encounter.getVitalSign().getOxygenLevel()));
        jLabel17.setText(String.valueOf(encounter.getDiagnosis()));

        Calendar c = Calendar.getInstance();
        c.setTime(encounter.getDate());

        // setting up the values from object of calender
        jComboBox1.setSelectedItem(String.valueOf(c.get(Calendar.DATE)));
        jComboBox2.setSelectedIndex(c.get(Calendar.MONTH));
        jComboBox3.setSelectedItem(String.valueOf(c.get(Calendar.YEAR)));

        String[] splitTime = encounter.getTime().split(" ");

        jComboBox4.setSelectedItem(String.valueOf(splitTime[0]));
        jComboBox5.setSelectedItem(String.valueOf(splitTime[1]));
    }//GEN-LAST:event_jButton1ActionPerformed

