    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String fname = firstBtn.getText();
        String lname = lastBtn.getText();
        String age = ageBtn.getText();
        String cell = cellBtn.getText();
        String address = jTextField1.getText();

        if (fname.isBlank() || fname.isEmpty() || lname.isBlank() || lname.isEmpty()
            || age.isBlank() || age.isEmpty() || cell.isBlank() || cell.isEmpty()
            || address.isBlank() || address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are mandatory");
            return;
        }

        try {
            int ageInt = Integer.parseInt(age);
            if (ageInt <= 0) {
                JOptionPane.showMessageDialog(this, "Age cannot be negative or 0");
                return;
            }
            this.patient.setAge(ageInt);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Age should be number");
            return;
        }

        if (cell.length() != 10) {
            JOptionPane.showMessageDialog(this, "Cell number should be 10 digits");
            return;
        }
        try {
            this.patient.setCellNumber(Long.parseLong(cell));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Cell should be number");
            return;
        }

        this.patient.setFname(fname);
        this.patient.setLname(lname);

        String gender = jComboBox1.getSelectedItem().toString();
        Gender g = null;
        g = switch (gender) {
            case "MALE" -> Gender.M;
            case "FEMALE" -> Gender.F;
            default -> Gender.OTHERS;
        };

        this.patient.setGender(g);
        this.patient.getResidence().setAddress(address);

        JOptionPane.showMessageDialog(this, "Successfully updated");
    }//GEN-LAST:event_jButton2ActionPerformed

