    private void populateFields() {
        firstBtn.setText(this.patient.getFname());
        lastBtn.setText(this.patient.getLname());
        ageBtn.setText(String.valueOf(this.patient.getAge()));
        cellBtn.setText(String.valueOf(this.patient.getCellNumber()));
        jTextField1.setText(this.patient.getResidence().getAddress());
        
        Gender g = this.patient.getGender();
        if (g == Gender.M) {
            jComboBox1.setSelectedIndex(0);
        }
        if (g == Gender.F) {
            jComboBox1.setSelectedIndex(1);
        }
        if (g == Gender.OTHERS) {
            jComboBox1.setSelectedIndex(2);
        }
        
    }

